    # 写入文件
    with open(temp_path, 'w') as f:
        f.write(inference_code)

    # 记录到 MLflow
    mlflow.log_artifact(temp_path, artifact_path=os.path.dirname(artifact_path) or ".")

    logger.info(f"Saved inference script to {artifact_path}")
    return temp_path


