def save_model_summary(
    model_info: Dict[str, Any],
    artifact_path: str = "model_summary.json",
) -> str:
    """
    保存模型架构摘要

    Args:
        model_info: 模型信息字典（包含架构、参数量、输入输出形状等）
        artifact_path: Artifact 路径

    Returns:
        本地临时文件路径
    """
    import tempfile

    # 创建临时文件
    temp_dir = tempfile.mkdtemp()
    temp_path = os.path.join(temp_dir, artifact_path)

    # 写入 JSON
    with open(temp_path, 'w') as f:
        json.dump(model_info, f, indent=2)

    # 记录到 MLflow
    mlflow.log_artifact(temp_path, artifact_path=os.path.dirname(artifact_path) or ".")

    logger.info(f"Saved model summary to {artifact_path}")
    return temp_path


