def save_inference_script(
    model_name: str,
    feature_names: list,
    preprocessing_steps: Optional[str] = None,
    artifact_path: str = "inference.py",
) -> str:
    """
    生成最小推理脚本

    Args:
        model_name: 模型名称
        feature_names: 特征名称列表
        preprocessing_steps: 预处理步骤说明
        artifact_path: Artifact 路径

    Returns:
        本地临时文件路径
    """
    import tempfile

    inference_code = f'''"""
Minimal inference script for {model_name}

Usage:
    python inference.py --input sample_input.json --output predictions.json
"""

import mlflow
import pandas as pd
import numpy as np
import json
import argparse
from pathlib import Path


# Expected feature names (in order)
FEATURE_NAMES = {feature_names[:10]}  # First 10 shown


def load_model(model_uri: str = "models:/{model_name}@champion"):
    """Load the champion model from MLflow"""
    return mlflow.pyfunc.load_model(model_uri)


def preprocess(data: dict) -> pd.DataFrame:
    """
    Preprocess input data

    {preprocessing_steps or "Note: Add preprocessing logic here"}
    """
    df = pd.DataFrame([data])
    # Ensure correct column order
    # df = df[FEATURE_NAMES]
    return df


def predict(model, input_data: dict) -> dict:
    """
    Run inference on input data

    Args:
        model: Loaded MLflow model
        input_data: Dict of feature values

    Returns:
        Dict with prediction and probabilities
    """
    # Preprocess
    df = preprocess(input_data)

    # Predict
    prediction = model.predict(df)[0]

    # Get probabilities if available
    try:
        proba = model.predict_proba(df)[0]
        result = {{
            "prediction": int(prediction),
            "probabilities": proba.tolist(),
            "confidence": float(np.max(proba))
        }}
    except AttributeError:
        result = {{
            "prediction": int(prediction)
        }}

    return result


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", type=str, required=True, help="Input JSON file")
    parser.add_argument("--output", type=str, default="predictions.json", help="Output JSON file")
    parser.add_argument("--model-uri", type=str, default="models:/{model_name}@champion", help="MLflow model URI")
    args = parser.parse_args()

    # Load model
    print(f"Loading model from {{args.model_uri}}...")
    model = load_model(args.model_uri)

    # Load input
    with open(args.input, 'r') as f:
        input_data = json.load(f)

    # Predict
    print("Running inference...")
    result = predict(model, input_data)

    # Save output
    with open(args.output, 'w') as f:
        json.dump(result, f, indent=2)

    print(f"Predictions saved to {{args.output}}")
    print(f"Predicted class: {{result['prediction']}}")


if __name__ == "__main__":
    main()
'''

    # 创建临时文件
